"""v0.5 hard gate: emitted schemas carry v0.5 URNs.

Per the spawn prompt: schema URN bumped on emit side from v0.4 to v0.5.
The bundled JSON Schema files at ``schemas/`` are the canonical write
surface that the Python SDK loads via ``concordia.schema_validator``.
This test asserts those schemas carry ``v0.5`` URNs and that
attestations generated by ``generate_attestation()`` validate cleanly
against them.
"""

from __future__ import annotations

import json
from pathlib import Path

import pytest

from concordia import (
    Agent,
    BasicOffer,
    SessionState,
    generate_attestation,
    is_valid_attestation,
)


_SCHEMAS_DIR = Path(__file__).resolve().parent.parent / "schemas"


def _load_schema(name: str) -> dict:
    with open(_SCHEMAS_DIR / name) as f:
        return json.load(f)


class TestSchemaUrnsAtV05:
    """Emit-side schemas carry v0.5 URNs per SPEC §11.5.6 / §11.5.6."""

    def test_attestation_schema_at_v05(self):
        schema = _load_schema("attestation.schema.json")
        assert schema["$id"] == "urn:concordia:schema:attestation:v0.5"

    def test_reference_schema_at_v05(self):
        schema = _load_schema("reference.schema.json")
        assert schema["$id"] == "urn:concordia:schema:reference:v0.5"

    def test_attestation_schema_embeds_v05_reference_def(self):
        schema = _load_schema("attestation.schema.json")
        ref_def = schema["$defs"]["reference"]
        assert "id" in ref_def["required"]
        assert "type" in ref_def["required"]
        assert "relationship" in ref_def["required"]


@pytest.fixture
def agreed_session():
    seller = Agent("seller_v05_emit")
    buyer = Agent("buyer_v05_emit")
    terms = {"price": {"value": 100.0, "currency": "USD"}, "qty": {"value": 1}}
    session = seller.open_session(counterparty=buyer.identity, terms=terms)
    buyer.join_session(session)
    buyer.accept_session()
    seller.send_offer(BasicOffer(terms={
        "price": {"value": 100.0, "currency": "USD"},
        "qty": {"value": 1},
    }))
    buyer.accept_offer()
    assert session.state == SessionState.AGREED
    return session, seller, buyer


def _key_pairs(seller, buyer):
    return {
        seller.identity.agent_id: seller.key_pair,
        buyer.identity.agent_id: buyer.key_pair,
    }


class TestGeneratedAttestationsConformToV05:
    """Attestations generated today validate against the v0.5 schemas."""

    def test_minimal_attestation_validates(self, agreed_session):
        session, seller, buyer = agreed_session
        att = generate_attestation(session, _key_pairs(seller, buyer))
        assert is_valid_attestation(att)

    def test_attestation_with_v05_optional_reference_fields(self, agreed_session):
        session, seller, buyer = agreed_session
        refs = [
            {
                "type": "receipt",
                "id": "att_prior",
                "relationship": "extends",
                "version": "0.4.0",
                "signed_at": "2026-04-20T12:00:00Z",
                "signer_did": "did:web:example.org:agent-7",
            },
        ]
        att = generate_attestation(
            session, _key_pairs(seller, buyer), references=refs
        )
        assert is_valid_attestation(att)
        emitted = att["references"][0]
        assert emitted["version"] == "0.4.0"
        assert emitted["signed_at"] == "2026-04-20T12:00:00Z"
        assert emitted["signer_did"] == "did:web:example.org:agent-7"
